# 定义 Bash 脚本模板
def generate_bash_script(num):
    bash_content = f"""#!/bin/bash
#SBATCH -p big,low,smp01,smp02  ##节点名称
#SBATCH -J vina{num}   ##任务名称
#SBATCH -N 1  ##节点
#SBATCH -n 16  ##线程
#SBATCH -o vina-{num}.o  ##输出信息
#SBATCH -e vina-{num}.e  ##报错信息输出

python 4_reserve_docking.py -num {num} --exhaustiveness 16 --log_dir /public/agis/yanjianbin_group/liuyugeng/taxus_alphaflow_docking/UDPGT/data_output/4_docking_results/file_{num}
"""
    return bash_content

# 主函数：生成并保存 .sh 文件
def generate_sh_files():
    for num in num_list:
        # 生成 Bash 脚本内容
        script_content = generate_bash_script(num)
        
        # 定义文件名和保存路径
        file_name = f"4_{num}_docking.sh"
        
        # 写入文件
        with open(file_name, 'w') as f:
            f.write(script_content)
        
        # 设置文件权限（可选，Linux 下可执行）
        os.chmod(file_name, 0o755)
        
        print(f"Generated: {file_name}")

if __name__ == "__main__":
    generate_sh_files()